    private void populateTable() {

        DefaultTableModel Model = (DefaultTableModel) jTable1.getModel();
        Model.setRowCount(0);

        Object row[] = new Object[10];
        CustDirectory CustDirec = systAdmin.getCustDirec(); //get all Custs
        for (Cust Cust : CustDirec.getListOfCust()) {
            for (Book Book : Cust.getBkList()) {      //get Book dtls each Cust
                for (Service service : Book.getServices()) {       //get services under Book

                    if (service.getEnterprise().getName().equals(HClub.getName())) {

                        HClubService HClubService = (HClubService) service;

                        row[0] = Book;
                        row[1] = Cust;
                        row[2] = HClubService.getStatus();
                        row[3] = "NO";
                        row[4] = "NO";
                        row[5] = "NO";

                        for (HClubService.HClubServiceType type : HClubService.getHClubServices()) {
                            switch (type) {
                                case Therapy:
                                    row[3] = "YES";
                                    break;
                                case Phy:
                                    row[4] = "YES";
                                    break;
                                case TRAINER:
                                    row[5] = "YES";
                                    break;
                            }
                        }
                        Model.addRow(row);
                    }
                    
                }
            }
        }

    }

    private void populateComboBox() {
        TherapyOrg.addItem(null);
        PhyOrg.addItem(null);
        trainerOrg.addItem(null);

        for (TherapyOrg Therapy : HClub.getListOfTherapyOrg()) {
            if (Therapy != null) {
                TherapyOrg.addItem(Therapy);
            }
        }
        for (PhyOrg Phy : HClub.getListOfPhyOrg()) {
            if (Phy != null) {
                PhyOrg.addItem(Phy);
            }
        }
        for (TrainerOrg trainer : HClub.getListOfTrainerOrg()) {
            if (trainer != null) {
                trainerOrg.addItem(trainer);
            }
        }
    }

