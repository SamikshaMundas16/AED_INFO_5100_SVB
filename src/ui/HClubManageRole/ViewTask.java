    private void assignWorkActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_assignWorkActionPerformed
        int selectedRowIndex = jTable1.getSelectedRow();
        if (selectedRowIndex < 0) {
            JOptionPane.showMessageDialog(this, "Please select a Book to assign tasks.");
            return;
        }

        DefaultTableModel Model = (DefaultTableModel) jTable1.getModel();
        Book Book = (Book) Model.getValueAt(selectedRowIndex, 0);

        HClubService HClubService = null;
        for (Service service : Book.getServices()) {
            if (HClub.getName().equals(service.getEnterprise().getName())) {
                HClubService = (HClubService) service;
                break;
            }
        }

        if (HClubService == null) {
            JOptionPane.showMessageDialog(this, "Cannot find health club");
            return;
        }
        if (!HClubService.getStatus().equals(Service.Status.PENDING)) {
            JOptionPane.showMessageDialog(this, String.format("Book '%s' should be 'PENDING' state to be accepted.",
                Book.getId()));
        return;
        }

        TherapyOrg Therapy = (TherapyOrg) TherapyOrg.getSelectedItem();
        PhyOrg Phy = (PhyOrg) PhyOrg.getSelectedItem();
        TrainerOrg trainer = (TrainerOrg) trainerOrg.getSelectedItem();

        List<Org> Orgs = new ArrayList<>();
        for (HClubService.HClubServiceType type : HClubService.getHClubServices()) {
            switch (type) {
                case Therapy:
                if (Therapy == null) {
                    JOptionPane.showMessageDialog(this, "Please select Therapy Org to be assinged for the Book.");
                    return;
                } else {
                    Orgs.add(Therapy);
                }
                break;
                case Phy:
                if (Phy == null) {
                    JOptionPane.showMessageDialog(this, "Please select Phy Org to be assinged for the Book.");
                    return;
                } else {
                    Orgs.add(Phy);
                }
                break;
                case TRAINER:
                if (trainer == null) {
                    JOptionPane.showMessageDialog(this, "Please select trainer Org to be assinged for the Book.");
                    return;
                } else {
                    Orgs.add(trainer);
                }
                break;
            }
        }

        for (Org Org : Orgs) {
            HClubService.addOrg(Org);
        }
        HClubService.setStatus(Service.Status.CONFIRMED);
        JOptionPane.showMessageDialog(this, "Assigned all HClub services to the Book: " + Book.getId());

        return;
    }//GEN-LAST:event_assignWorkActionPerformed

