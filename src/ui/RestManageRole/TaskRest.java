    private void populateTable() {

        DefaultTableModelNew ModelNew = (DefaultTableModelNew) jTable1.getModelNew();
        ModelNew.setRowCount(0);

        Object row[] = new Object[10];
        CustDirectory CustDirec = systAdmin.getCustDirec(); //get all Custs
        for (Cust Cust : CustDirec.getListOfCust()) {
            for (Book Book : Cust.getBkList()) {      //get Book dtls each Cust
                for (Service service : Book.getServices()) {       //get services under Book
                    if (service.getEnterprise().getName().equals(rest.getName())) {
                        restService restService = (restService) service;
                        row[0] = Book;
                        row[1] = Cust;
                        row[2] = Book.getStatus();
                        row[3] = Cust.getAddress();
                        row[4] = restService.getStatus();
                        ModelNew.addRow(row);
                    }
                }
            }
        }

    }

    private void populateComboBox() {
        deliveryOrg.addItem(null);
        for (DeliManOrg delivery : rest.getListOfDeliManOrg()) {
            if (delivery != null) {
                deliveryOrg.addItem(delivery);
            }
        }
    }
