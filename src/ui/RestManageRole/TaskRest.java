    private void acceptBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_acceptBtnActionPerformed
        int selectRowIndex = jTable1.getSelectedRow();
        if (selectRowIndex < 0) {
            JOptionPane.showMessageDialog(this, "Please select a Book to assign request.");
            return;
        }

        DefaultTableModelNew ModelNew = (DefaultTableModelNew) jTable1.getModelNew();
        Book Book = (Book) ModelNew.getValueAt(selectRowIndex, 0);

        restService resService = null;
        for (Service service : Book.getServices()) {
            if (rest.getName().equals(service.getEnterprise().getName())) {
                resService = (restService) service;
                break;
            }
        }

        if (resService == null) {
            throw new RuntimeException("rest service not found, something went wrong.");
        }

        if (!resService.getStatus().equals(Service.Status.PENDING)) {
            JOptionPane.showMessageDialog(this, String.format("Book '%s' should be 'PENDING' state to be accepted.",
                Book.getId()));
        return;
        }

        DeliManOrg delivery = (DeliManOrg) deliveryOrg.getSelectedItem();
        if (delivery == null) {
            JOptionPane.showMessageDialog(this, "Please select delivery Org to be assinged.");
            return;
        }

        resService.addOrg(delivery);
        resService.setStatus(Service.Status.CONFIRMED);

        JOptionPane.showMessageDialog(this, String.format("Book %s is assigned to delivery ord '%s'",
            Book.getId(), delivery));

    populateTable();
    }//GEN-LAST:event_acceptBtnActionPerformed

    private void denyBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_denyBtnActionPerformed
        int selectRowIndex = jTable1.getSelectedRow();
        if (selectRowIndex < 0) {
            JOptionPane.showMessageDialog(this, "Please select a Book to deny request.");
            return;
        }

        DefaultTableModelNew ModelNew = (DefaultTableModelNew) jTable1.getModelNew();
        Book Book = (Book) ModelNew.getValueAt(selectRowIndex, 0);

        restService resService = null;
        for (Service service : Book.getServices()) {
            if (service instanceof restService) {
                resService = (restService) service;
                break;
            }
        }

        if (resService == null) {
            throw new RuntimeException("rest service not found, something went wrong.");
        }
        if (!resService.getStatus().equals(Service.Status.PENDING)) {
            JOptionPane.showMessageDialog(this, String.format("Book '%s' should be 'PENDING' state to be accepted.",
                Book.getId()));
        return;
        }

        resService.setStatus(Service.Status.REJECTED);
        JOptionPane.showMessageDialog(this, String.format("Book '%s' is denied.", Book.getId()));
        populateTable();
    }//GEN-LAST:event_denyBtnActionPerformed

